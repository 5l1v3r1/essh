func HostsByNames(names []string) []*Host {
	var hosts = []*Host{}

	for _, host := range SortedHosts() {
	B1:
		for _, name := range names {
			if host.Name == name {
				hosts = append(hosts, host)
				break B1
			}
		}

	B2:
		for _, tag := range host.Tags {
			for _, name := range names {
				if tag == name {
					hosts = append(hosts, host)
					break B2
				}
			}
		}
	}

	return hosts
}

func HostsByTag(name string, isOnlyPublic bool) []*Host {
	var hosts = []*Host{}

	for _, host := range SortedHosts() {
	B1:
		for _, tag := range host.Tags {
			if tag == name {
				if isOnlyPublic {
					if !host.Private {
						hosts = append(hosts, host)
					}
				} else {
					hosts = append(hosts, host)
					break B1
				}
			}
		}
	}

	return hosts
}

func ResetHosts() {
	LocalHosts = map[string]*Host{}
	GlobalHosts = map[string]*Host{}
	PublicHosts = map[string]*Host{}

}
